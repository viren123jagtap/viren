class Overloading

{
public static void Virendra()
{
System.out.println("Method virendra");
}

public static void Jagtap()
{
System.out.println("method jagtap");
}

public static void main(String[] args)
{

System.out.println("Method start");
Virendra();
Jagtap();
System.out.println("Method end");

}
}